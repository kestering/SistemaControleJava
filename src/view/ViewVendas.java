    private void listarClientes() {
        listaModelClientes = controllerCliente.getListaClienteController();
        comboBoxCliente.removeAllItems();
        for (int i = 0; i < listaModelClientes.size(); i++) {
            comboBoxCliente.addItem(listaModelClientes.get(i).getNomeCliente());
        }
    }

    private void listarProdutos() {
        listaModelProdutos = controllerProduto.retornarListaProdutoController();
        comboBoxNomeProduto.removeAllItems();
        for (int i = 0; i < listaModelProdutos.size(); i++) {
            comboBoxNomeProduto.addItem(listaModelProdutos.get(i).getNome_produto());
        }

    }

    private void carregarVendas() {
        DefaultTableModel modelo = (DefaultTableModel) tableVendas.getModel();
        listaModelVendasClientes = controllerVendasCliente.getListaVendasClienteController();
        int cont = listaModelVendasClientes.size();
        modelo.setNumRows(0);
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelVendasClientes.get(i).getModelVendas().getIdVendas(),
                listaModelVendasClientes.get(i).getModelCliente().getNomeCliente(),
                listaModelVendasClientes.get(i).getModelVendas().getData_vendas()

            });
        }
    }

    private void somarValotTotalProdutos() {
        double soma = 0, valor;
        int cont = tableVendasProdutos.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = (double) tableVendasProdutos.getValueAt(i, 4);
            soma = soma + valor;
        }
        txtValorTotal.setText(String.valueOf(soma));
        aplicarDesconto();
    }

    private void aplicarDesconto() {
        try {
            txtValorTotal.setText(String.valueOf(Double.parseDouble(txtValorTotal.getText()) - Double.parseDouble(txtDesconto.getText())));
        } catch (NumberFormatException e) {
        }

    }

    private void limparFormulario() {
        txtQuantidade.setText("");
        txtDesconto.setText("");
        txtValorTotal.setText("");

        DefaultTableModel modelo = (DefaultTableModel) tableVendasProdutos.getModel();
        modelo.setNumRows(0);

    }

