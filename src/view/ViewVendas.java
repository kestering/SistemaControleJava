    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        int codigoVenda = 0, codigoProduto = 0;
        double desconto = 0;
        listaModelVendasProdutos = new ArrayList<>();

        if (txtDesconto.getText().equals("")) {
            desconto = 0;
        } else {
            desconto = Double.parseDouble(txtDesconto.getText());
        }

        modelVendas.setCliente(Integer.parseInt(txtCodCliente.getText()));
        try {
            modelVendas.setData_vendas(blDatas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception ex) {
            Logger.getLogger(ViewVendas.class.getName()).log(Level.SEVERE, null, ex);
        }
        modelVendas.setValorTotal_vendas(Double.parseDouble(txtValorTotal.getText()));
        modelVendas.setValor_vendas(Double.parseDouble(txtValorTotal.getText()) + desconto);
        modelVendas.setDesconto_vendas(desconto);

        codigoVenda = controllerVendas.salvarVendasController(modelVendas);
        if (codigoVenda < 0) {
            JOptionPane.showMessageDialog(this, "Erro ao concluir a venda!");
        } else {
            JOptionPane.showMessageDialog(this, "Venda efetuada com Sucesso!");

        }

        int cont = tableVendasProdutos.getRowCount();
        for (int i = 0; i < cont; i++) {
            codigoProduto = (int) tableVendasProdutos.getValueAt(i, 0);
            modelVendasProdutos = new ModelVendasProdutos();
            modelProdutos = new ModelProdutos();
            modelVendasProdutos.setIdProduto(codigoProduto);
            modelVendasProdutos.setIdVendas(Integer.parseInt(txtCodCliente.getText()));
            modelVendasProdutos.setValor_vendaproduto((Double) tableVendasProdutos.getValueAt(i, 3));
            modelVendasProdutos.setQuantidade_vendaproduto(Integer.parseInt(tableVendasProdutos.getValueAt(i, 2).toString()));

            modelProdutos.setIdProduto(codigoProduto);
            modelProdutos.setEstoque_produto(controllerProduto.retornarProdutoController(codigoProduto).getEstoque_produto()
                    - Integer.parseInt(tableVendasProdutos.getValueAt(i, 2).toString()));
            listaModelVendasProdutos.add(modelVendasProdutos);
            listaModelProdutos.add(modelProdutos);
        }

        if (controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutos)) {

            controllerProduto.alterarEstoqueProdutoController(listaModelProdutos);
            
            carregarVendas();
            limparFormulario();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao adicionar a venda!");
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

