    private void carregarCliente() {
        listaModelCliente = controllerCliente.getListaClienteController();
        DefaultTableModel modelo = (DefaultTableModel) tableCliente.getModel();
        modelo.setNumRows(0);
        //inserir os produtos na tabela
        int cont = listaModelCliente.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelCliente.get(i).getIdCliente(),
                listaModelCliente.get(i).getNomeCliente(),
                listaModelCliente.get(i).getCidadeCliente(),
                listaModelCliente.get(i).getTelefoneCliente(),});
        }
    }

    private void habilitarCampos(boolean condicao) {
        txtNome.setEnabled(condicao);
        txtEndereco.setEnabled(condicao);
        txtBairro.setEnabled(condicao);
        txtCidade.setEnabled(condicao);
        jComboBox_UF.setEnabled(condicao);
        txtCep.setEnabled(condicao);
        txtTelefone.setEnabled(condicao);
        botao_Salvar.setEnabled(condicao);

    }

    private void salvarCliente() {
        salvarAlterar = "salvar";
        modelCliente.setNomeCliente(this.txtNome.getText());
        modelCliente.setEnderecoCliente(this.txtEndereco.getText());
        modelCliente.setBairroCliente(this.txtBairro.getText());
        modelCliente.setCidadeCliente(this.txtCidade.getText());
        modelCliente.setUfCliente(this.jComboBox_UF.getSelectedItem().toString());
        modelCliente.setCepCliente(this.txtCep.getText());
        modelCliente.setTelefoneCliente(this.txtTelefone.getText());
        controllerCliente.salvarClienteController(modelCliente);
        JOptionPane.showMessageDialog(this, "Cliente Cadastrado com sucesso!");
        this.carregarCliente();

        limparCampos();

    }

    private void alterarCliente() {

        salvarAlterar = "alterar";
        modelCliente.setNomeCliente(this.txtNome.getText());
        modelCliente.setEnderecoCliente(this.txtEndereco.getText());
        modelCliente.setBairroCliente(this.txtBairro.getText());
        modelCliente.setCidadeCliente(this.txtCidade.getText());
        modelCliente.setUfCliente(this.jComboBox_UF.getSelectedItem().toString());
        modelCliente.setCepCliente(this.txtCep.getText());
        modelCliente.setTelefoneCliente(this.txtTelefone.getText());
        controllerCliente.atualizarClienteController(modelCliente);
        JOptionPane.showMessageDialog(this, "Cliente alterado com sucesso!");
        this.carregarCliente();

        limparCampos();

    }

    private void limparCampos() {
        this.txtCodigo.setText("");
        this.txtNome.setText("");
        this.txtEndereco.setText("");
        this.txtBairro.setText("");
        this.txtCidade.setText("");
        this.txtCep.setText("");
        this.txtTelefone.setText("");

    }

