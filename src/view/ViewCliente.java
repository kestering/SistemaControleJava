    private void botao_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_SalvarActionPerformed

        if (salvarAlterar.equals("salvar")) {
            this.salvarCliente();
            habilitarCampos(false);
            limparCampos();

        } else if (salvarAlterar.equals("alterar")) {
            this.alterarCliente();
            habilitarCampos(false);
            limparCampos();

        }
        limparCampos();
    }//GEN-LAST:event_botao_SalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        int linha = tableCliente.getSelectedRow();
        int codigoCliente = (int) tableCliente.getValueAt(linha, 0);

        if (controllerCliente.excluirClienteController(codigoCliente)) {
            JOptionPane.showMessageDialog(this, "Cliente Excluido com sucesso!", "AVISO!", JOptionPane.WARNING_MESSAGE);
            this.carregarCliente();

        } else {
            JOptionPane.showMessageDialog(this, "Erro ao Excluir Cliente!", "ERROR!", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void botao_AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_AlterarActionPerformed

        habilitarCampos(true);
        salvarAlterar = "alterar";
        int linha = tableCliente.getSelectedRow();
        try {
            int codigo = (int) tableCliente.getValueAt(linha, 0);
            modelCliente = controllerCliente.getClienteController(codigo);

            this.txtCodigo.setText(String.valueOf(modelCliente.getIdCliente()));
            this.txtNome.setText(modelCliente.getNomeCliente());
            this.txtEndereco.setText(String.valueOf(modelCliente.getEnderecoCliente()));
            this.txtBairro.setText(String.valueOf(modelCliente.getBairroCliente()));
            this.txtCidade.setText(String.valueOf(modelCliente.getCidadeCliente()));
            this.jComboBox_UF.setSelectedItem(modelCliente.getUfCliente());
            this.txtCep.setText(String.valueOf(modelCliente.getCepCliente()));
            this.txtTelefone.setText(String.valueOf(modelCliente.getTelefoneCliente()));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código invalido ou nenhum registro selecionado!", "AVISO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botao_AlterarActionPerformed

